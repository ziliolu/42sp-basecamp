void	copy_table(int grid[4][4], int copy_grid[4][4])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			copy_grid[i][j] = grid[i][j];
			j++;
		}
		j = 0;
		i++;
	}
}
