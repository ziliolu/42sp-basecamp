void	kick_possible_n(int grid[4][4], int k)
{
	int	i;
	int	j;
	int	len;

	i = 0;
	j = 0;
	len = 4;
	while (i < len)
	{
		while (j < len)
		{
			if (grid[i][j] == 0)
			{
				grid[i][j] = k;
				return ;
			}
			j++;
		}
		j = 0;
		i++;
	}
}
